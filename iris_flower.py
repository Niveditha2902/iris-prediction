from sklearn.datasets import load_iris
from sklearn.model_selection import train_test_split
from sklearn.neighbors import KNeighborsClassifier
import numpy as np

iris=load_iris()
print("Target names: {}".format(iris['target_names']))
print("Feature names: {}".format(iris['feature_names']))
print("Type of data: {}".format(type(iris['data'])))
print("Shape of data: {}".format(iris['data'].shape))

print("shape of Target: {}".format(iris['target_names'].shape))
print("target values: {}".format(iris['target']))
print("data shape: {}".format(iris['data'].shape))

x_train,x_test,y_train,y_test=train_test_split(iris['data'],iris['target'],random_state=1)

print("X Training data shape: {}".format(x_train.shape))
print("Y Training data shape: {}".format(y_train.shape))

print("X testing data shape : {}".format(x_test.shape))
print("Y testing data shape : {}".format(y_test.shape))

knn=KNeighborsClassifier(n_neighbors=1)
knn.fit(x_train,y_train)

new = np.array([[6.5 ,3. , 5.2 ,2. ]])
predi=knn.predict(x_test)

print(predi)
print("Predicted flower type is : {}".format(iris['target_names'][predi]))

y_pred=knn.predict(x_test)
print("TEsting score: {:.2f}".format(np.mean(y_pred==y_test)))

print(knn.score(x_test,y_test))